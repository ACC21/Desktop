      Nodes closer to the root are called (grand)<b>parents</b>&#160;of nodes further away called
    </p>
    <p>
      (grand)<b>children</b>. Nodes which ar at the same level, but not mutually connected,
    </p>
    <p>
      are called&#160; <b>siblings</b>.
    </p>
